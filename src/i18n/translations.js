const resolveTranslation = (lang, keys) => {
  let value = translations[lang]
  for (const k of keys) {
    value = value?.[k]
    if (value === undefined) {
      return undefined
    }
  }
  return value
}

export const getTranslation = (lang, key) => {
  const keys = key.split(".")
  const value = resolveTranslation(lang, keys)
  if (value !== undefined) {
    return value
  }

  const fallback = lang !== "en" ? resolveTranslation("en", keys) : undefined
  if (fallback !== undefined) {
    return fallback
  }

  return key
}
